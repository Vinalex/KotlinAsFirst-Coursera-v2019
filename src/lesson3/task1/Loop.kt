 * Использовать операции со строками в этой задаче запрещается.
 */
fun isPalindrome(n: Int): Boolean = n == revert(n)

/**
 * Средняя
 *
 * Для заданного числа n определить, содержит ли оно различающиеся цифры.
 * Например, 54 и 323 состоят из разных цифр, а 111 и 0 из одинаковых.
 *
 * Использовать операции со строками в этой задаче запрещается.
 */
fun hasDifferentDigits(n: Int): Boolean {
    var num = n
    var r: Int
    val r0 = num % 10
    while (num > 0) {
        r = num % 10
        num /= 10
        if (r != r0) return true
    }
    return false
}

fun createDigit(d1: Int, d2: Int): Int = d1 * 10.0.pow(digitNumber(d2)).toInt() + d2

fun numByPosition(d: Int, position: Int, rev: Boolean = false): Int {
    var dd = if (rev) revert(d) else d
    var result: Int = -1
    for (i in 1..position) {
        result = dd % 10
        dd /= 10
    }
    return result
}

/**
 * Сложная
 *
 * Найти n-ю цифру последовательности из квадратов целых чисел:
 * 149162536496481100121144...
 * Например, 2-я цифра равна 4, 7-я 5, 12-я 6.
 *
 * Использовать операции со строками в этой задаче запрещается.
 */
fun squareSequenceDigit(n: Int): Int {
    var dSqr: Int
    var dLen: Int
    var nn = n
    for (i in 1..n) {
        dSqr = sqr(i)
        dLen = digitNumber(dSqr)
        if (dLen >= nn) {
            return numByPosition(dSqr, nn, rev = true)
        }
        nn -= dLen
    }
    return -1
}

